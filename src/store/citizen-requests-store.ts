import { create } from 'zustand'
import { persist } from 'zustand/middleware'

export type RequestStatus = 'soumise' | 'en_cours' | 'pieces_complementaires' | 'validee' | 'prete' | 'livree' | 'rejetee'

// ─── New interface for uploaded files ───────────────────────────────────────
export interface UploadedDocument {
  id: string
  name: string          // Original filename
  type: string          // MIME type (e.g., 'application/pdf', 'image/png')
  size: number          // File size in bytes
  data: string          // Base64 encoded file data
  uploadedAt: string    // ISO date
  requiredDocName: string // Which required document this fulfills
  verified: boolean     // Whether agent has verified this document
}

// ─── New interface for generated official documents ─────────────────────────
export interface GeneratedDocument {
  id: string
  title: string
  htmlContent: string   // Full HTML content for the official document
  generatedAt: string
  generatedBy: string   // Agent name
  fileName: string      // Download filename
}

export interface SatisfactionRating {
  rating: 1 | 2 | 3 | 4 | 5
  comment: string
  ratedAt: string
}

// ─── AI Processing Detail ────────────────────────────────────────────────────
export interface AIPProcessingDetail {
  step: string
  status: 'success' | 'warning' | 'error' | 'info'
  message: string
  timestamp: string
  duration?: number  // ms
}

// ─── Attached File (for AI agent processing) ─────────────────────────────────
export interface AttachedFile {
  id: string
  name: string
  size: number
  type: string
  category: 'justificatif' | 'complement' | 'photo' | 'autre'
  uploadedAt: string
  verified: boolean
}

export interface CitizenRequest {
  id: string
  reference: string
  serviceId: string
  serviceName: string
  category: string
  categoryId: string
  // Citoyen
  citizenName: string
  citizenFirstName: string
  citizenNIN: string
  citizenPhone: string
  citizenEmail: string
  citizenAddress: string
  // Demande
  motif: string
  documents: string[]
  uploadedDocuments: UploadedDocument[]  // Files uploaded by the citizen
  generatedDocument?: GeneratedDocument  // The official document generated by the agent
  satisfaction?: SatisfactionRating
  // Traitement
  status: RequestStatus
  assignedService: string
  assignedAgent: string
  processingNotes: ProcessingNote[]
  timeline: TimelineStep[]
  // Dates
  createdAt: string
  updatedAt: string
  completedAt?: string
  // Mairie
  mairie?: string  // Specific mairie name (e.g., "Mairie de Kaloum") for etat-civil/residence requests
  // Livraison
  deliveryMode: 'en_ligne' | 'guichet' | 'courrier'
  deliveryLocation?: string
  documentUrl?: string
  // IA
  aiProcessingStatus?: 'none' | 'ai_pending' | 'ai_processing' | 'ai_completed' | 'ai_failed' | 'ai_auto_validated' | 'ai_auto_rejected' | 'ai_assisted'
  aiConfidence?: number
  aiProcessingDate?: string
  aiProcessingDetails?: AIPProcessingDetail[]
  attachedFiles?: AttachedFile[]
}

export interface ProcessingNote {
  id: string
  author: string
  authorRole: string
  text: string
  date: string
  type: 'note' | 'decision' | 'info_complementaire' | 'notification'
}

export interface TimelineStep {
  label: string
  status: 'completed' | 'current' | 'pending'
  date?: string
  agent?: string
}

