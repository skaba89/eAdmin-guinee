interface CitizenRequestsState {
  requests: CitizenRequest[]
  addRequest: (req: Omit<CitizenRequest, 'id' | 'reference' | 'status' | 'timeline' | 'processingNotes' | 'updatedAt' | 'assignedService' | 'assignedAgent'>) => CitizenRequest
  updateRequestStatus: (id: string, status: RequestStatus, note?: string) => void
  addProcessingNote: (id: string, note: Omit<ProcessingNote, 'id' | 'date'>) => void
  advanceTimeline: (id: string) => void
  assignRequest: (id: string, agent: string) => void
  completeRequest: (id: string, deliveryMode: CitizenRequest['deliveryMode'], deliveryLocation?: string) => void
  getRequestById: (id: string) => CitizenRequest | undefined
  getRequestByReference: (ref: string) => CitizenRequest | undefined
  getRequestsByCategory: (categoryId: string) => CitizenRequest[]
  getRequestsByStatus: (status: RequestStatus) => CitizenRequest[]
  addUploadedDocument: (id: string, doc: UploadedDocument) => void
  removeUploadedDocument: (requestId: string, docId: string) => void
  verifyDocument: (requestId: string, docId: string) => void
  setGeneratedDocument: (id: string, doc: GeneratedDocument) => void
  rateRequest: (id: string, rating: SatisfactionRating) => void
  resetToDemoData: () => void
  // AI Agent methods
  aiAutoProcess: (id: string) => void
  aiAutoProcessAll: () => void
  updateRequestAIFields: (id: string, fields: Partial<Pick<CitizenRequest, 'aiProcessingStatus' | 'aiConfidence' | 'aiProcessingDate' | 'aiProcessingDetails' | 'status' | 'assignedAgent' | 'processingNotes' | 'timeline' | 'updatedAt'>>) => void
}

export const useCitizenRequestsStore = create<CitizenRequestsState>()(
  persist(
    (set, get) => ({
      requests: DEMO_REQUESTS,

      addRequest: (req) => {
        const id = `req-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`
        const reference = generateReference()
        const nowIso = new Date().toISOString()
        const assignedService = SERVICE_ENTITY_MAP[req.categoryId] || 'Administration Générale'

        // Auto-set mairie for etat-civil and residence requests
        let mairie = req.mairie
        if ((req.categoryId === 'etat-civil' || req.categoryId === 'residence') && !mairie) {
          const communeMatch = req.citizenAddress.match(/Commune de\s+(.+)/i)
          if (communeMatch) {
            mairie = `Mairie de ${communeMatch[1].trim()}`
          } else {
            mairie = 'Mairie de Kaloum'
          }
        }

        const newRequest: CitizenRequest = {
          ...req,
          mairie,
          id,
          reference,
          status: 'soumise',
          assignedService,
          assignedAgent: '',
          timeline: generateTimeline(req.categoryId),
          processingNotes: [{
            id: `note-${Date.now()}`,
            author: 'Système',
            authorRole: 'Automate',
            text: `Demande soumise avec succès. Référence: ${reference}. Service compétent: ${assignedService}`,
            date: nowIso,
            type: 'notification',
          }],
          updatedAt: nowIso,
        }

        set((state) => ({ requests: [newRequest, ...state.requests] }))
        return newRequest
      },

      updateRequestStatus: (id, status, note) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? {
                  ...r,
                  status,
                  updatedAt: new Date().toISOString(),
                  completedAt: status === 'livree' ? new Date().toISOString() : r.completedAt,
                  processingNotes: note
                    ? [...r.processingNotes, {
                        id: `note-${Date.now()}`,
                        author: 'Agent traitant',
                        authorRole: 'Agent',
                        text: note,
                        date: new Date().toISOString(),
                        type: 'decision' as const,
                      }]
                    : r.processingNotes,
                }
              : r
          ),
        }))
      },

      addProcessingNote: (id, note) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? {
                  ...r,
                  updatedAt: new Date().toISOString(),
                  processingNotes: [...r.processingNotes, {
                    ...note,
                    id: `note-${Date.now()}`,
                    date: new Date().toISOString(),
                  }],
                }
              : r
          ),
        }))
      },

      advanceTimeline: (id) => {
        set((state) => ({
          requests: state.requests.map(r => {
            if (r.id !== id) return r
            const currentIdx = r.timeline.findIndex(s => s.status === 'current')
            const completedIdx = r.timeline.findIndex(s => s.status === 'completed')
            // If no current, first pending becomes current
            if (currentIdx === -1) {
              const firstPending = r.timeline.findIndex(s => s.status === 'pending')
              if (firstPending === -1) return r
              const newTimeline = r.timeline.map((step, i) =>
                i === firstPending ? { ...step, status: 'current' as const, date: new Date().toISOString() } : step
              )
              return { ...r, timeline: newTimeline, updatedAt: new Date().toISOString() }
            }
            // Complete current, advance next
            const newTimeline = r.timeline.map((step, i) => {
              if (i === currentIdx) return { ...step, status: 'completed' as const, date: new Date().toISOString() }
              if (i === currentIdx + 1 && step.status === 'pending') return { ...step, status: 'current' as const }
              return step
            })
            return { ...r, timeline: newTimeline, updatedAt: new Date().toISOString() }
          }),
        }))
      },

      assignRequest: (id, agent) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? { ...r, assignedAgent: agent, updatedAt: new Date().toISOString() }
              : r
          ),
        }))
      },

      completeRequest: (id, deliveryMode, deliveryLocation) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? {
                  ...r,
                  status: 'livree' as const,
                  deliveryMode,
                  deliveryLocation,
                  completedAt: new Date().toISOString(),
                  updatedAt: new Date().toISOString(),
                  timeline: r.timeline.map(step => ({
                    ...step,
                    status: 'completed' as const,
                    date: step.date || new Date().toISOString(),
                  })),
                }
              : r
          ),
        }))
      },

      addUploadedDocument: (id, doc) => {
        // localStorage protection: trim base64 data if total stored size would exceed ~4MB
        const MAX_LOCALSTORAGE_PER_DOC = 2 * 1024 * 1024 // 2MB per document
        const safeDoc = doc.data.length > MAX_LOCALSTORAGE_PER_DOC
          ? { ...doc, data: doc.data.slice(0, MAX_LOCALSTORAGE_PER_DOC), _truncated: true as any }
          : doc

        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? {
                  ...r,
                  uploadedDocuments: [...r.uploadedDocuments, safeDoc],
                  updatedAt: new Date().toISOString(),
                }
              : r
          ),
        }))
      },

      removeUploadedDocument: (requestId, docId) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === requestId
              ? {
                  ...r,
                  uploadedDocuments: r.uploadedDocuments.filter(d => d.id !== docId),
                  updatedAt: new Date().toISOString(),
                }
              : r
          ),
        }))
      },

      verifyDocument: (requestId, docId) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === requestId
              ? {
                  ...r,
                  uploadedDocuments: r.uploadedDocuments.map(d =>
                    d.id === docId ? { ...d, verified: true } : d
                  ),
                  updatedAt: new Date().toISOString(),
                }
              : r
          ),
        }))
      },

      setGeneratedDocument: (id, doc) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? {
                  ...r,
                  generatedDocument: doc,
                  updatedAt: new Date().toISOString(),
                }
              : r
          ),
        }))
      },

      rateRequest: (id, rating) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? { ...r, satisfaction: rating, updatedAt: new Date().toISOString() }
              : r
          ),
        }))
      },

      resetToDemoData: () => {
        set({ requests: DEMO_REQUESTS })
      },

      // AI Agent: Auto-process a single request with simulated AI decision
      aiAutoProcess: (id) => {
        const req = get().requests.find(r => r.id === id)
        if (!req) return

        // Simple simulated AI logic: if documents are complete, validate; otherwise request more info
        const uploadedCount = req.uploadedDocuments?.length ?? 0
        const requiredCount = req.documents?.length ?? 0
        const verifiedCount = (req.uploadedDocuments ?? []).filter(d => d.verified).length

        let aiStatus: CitizenRequest['aiProcessingStatus']
        let newStatus: RequestStatus = req.status
        const now = new Date().toISOString()
        const confidence = Math.min(95, 50 + (verifiedCount * 15) + (uploadedCount >= requiredCount ? 20 : 0))

        if (uploadedCount >= requiredCount && verifiedCount >= requiredCount) {
          // All docs uploaded and verified -> auto validate
          aiStatus = 'ai_auto_validated'
          newStatus = 'validee'
        } else if (uploadedCount >= requiredCount && verifiedCount < requiredCount) {
          // Docs uploaded but not all verified -> assisted
          aiStatus = 'ai_assisted'
        } else if (uploadedCount === 0) {
          // No docs -> auto reject
          aiStatus = 'ai_auto_rejected'
          newStatus = 'rejetee'
        } else {
          // Some docs -> assisted
          aiStatus = 'ai_assisted'
        }

        const details: AIPProcessingDetail[] = [
          { step: 'Vérification IA', status: 'success', message: `Analyse des documents: ${uploadedCount}/${requiredCount} chargés, ${verifiedCount} vérifiés`, timestamp: now, duration: 150 },
          { step: 'Décision IA', status: aiStatus === 'ai_auto_validated' ? 'success' : aiStatus === 'ai_auto_rejected' ? 'error' : 'warning', message: `Confiance: ${confidence}%. ${aiStatus === 'ai_auto_validated' ? 'Validation automatique' : aiStatus === 'ai_auto_rejected' ? 'Rejet: documents manquants' : 'Escalade: vérification humaine requise'}`, timestamp: now, duration: 200 },
        ]

        const aiNote: ProcessingNote = {
          id: `note-ai-${Date.now()}`,
          author: 'Agent IA',
          authorRole: 'IA',
          text: `Traitement IA automatique: Confiance ${confidence}%. ${aiStatus === 'ai_auto_validated' ? 'Demande validée automatiquement.' : aiStatus === 'ai_auto_rejected' ? 'Rejetée: pièces justificatives manquantes.' : 'Escalade vers agent humain pour vérification.'}`,
          date: now,
          type: 'decision',
        }

        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? {
                  ...r,
                  status: newStatus,
                  aiProcessingStatus: aiStatus,
                  aiConfidence: confidence,
                  aiProcessingDate: now,
                  aiProcessingDetails: details,
                  assignedAgent: r.assignedAgent || 'Agent IA',
                  processingNotes: [...r.processingNotes, aiNote],
                  updatedAt: now,
                }
              : r
          ),
        }))
      },

      // AI Agent: Auto-process all pending requests
      aiAutoProcessAll: () => {
        const pendingRequests = get().requests.filter(
          r => r.status === 'soumise' && (!r.aiProcessingStatus || r.aiProcessingStatus === 'none' || r.aiProcessingStatus === 'ai_pending')
        )
        pendingRequests.forEach(r => get().aiAutoProcess(r.id))
      },

      // Update AI fields on a request (used by ai-agent-store for real AI processing)
      updateRequestAIFields: (id, fields) => {
        set((state) => ({
          requests: state.requests.map(r =>
            r.id === id
              ? { ...r, ...fields, updatedAt: fields.updatedAt || new Date().toISOString() }
              : r
          ),
        }))
      },

      getRequestById: (id) => get().requests.find(r => r.id === id),
      getRequestByReference: (ref) => get().requests.find(r => r.reference === ref || r.reference.includes(ref)),
      getRequestsByCategory: (categoryId) => get().requests.filter(r => r.categoryId === categoryId),
      getRequestsByStatus: (status) => get().requests.filter(r => r.status === status),
    }),
    {
      name: 'citizen-requests-storage',
      version: 7,
      migrate: (persistedState: any, version: number) => {
        if (version < 7) {
          return { requests: DEMO_REQUESTS }
        }
        return persistedState
      },
    }
  )
)
